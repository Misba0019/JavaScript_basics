let num = 10;
let str = "20";

//String Concatenation
let result1 = num + str;
console.log(result1); // "1020"

//Numeric Conversion
let result2 = str - num;
console.log(result2); // 10

// Note:-
// When a number and a string are added, JavaScript converts the number to a string and concatenates the two strings together.
// When a number and a string are subtracted, JavaScript converts the string to a number and then performs the subtraction.
