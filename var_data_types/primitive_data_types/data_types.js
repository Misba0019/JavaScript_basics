// These are the Data Types:-
// String
const animalName = "Elephant";
console.log("Animal Name:", animalName); 
// Animal Name: Elephant

// Number
const numberOfLegs = 4;
console.log("Number of Legs:", numberOfLegs); 
// Number of Legs: 4

// Boolean
const isHerbivore = true;
console.log("Is Herbivore:", isHerbivore); 
// Is Herbivore: true

// Null
const predator = null;
console.log("Predator:", predator); 
// Predator: null

// Undefined
let habitat;
console.log("Habitat:", habitat); 
// Habitat: undefined

// Symbol
const uniqueAnimalID = Symbol("elephantID");
console.log("Unique Animal ID:", uniqueAnimalID); 
// Unique Animal ID: Symbol(elephantID)
